// -------------------------------
// Function for get element by id
// -------------------------------
function ByID(VALUE) {
    return document.getElementById(VALUE);
}

// -------------------------------
// Function for get element by query selector
// -------------------------------
function ByQuery(VALUE) {
    return document.querySelector(VALUE);
}

// -------------------------------
// Function for get element by query selector all
// -------------------------------
function ByQueryAll(VALUE) {
    return document.querySelectorAll(VALUE);
}

// -------------------------------
// Function for get element by Tag Name
// -------------------------------
function ByTagName(VALUE) {
    return document.getElementsByTagName(VALUE);
}


// -------------------------------
// Function for Create New Element
// -------------------------------
function createEle(VALUE) {
    return document.createElement(VALUE);
}
